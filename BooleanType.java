public class BooleanType {
    public static void main(String[] args) {
        boolean verificar = 0 < 1;
        System.out.println("0 é menor que 1? " + verificar);
        boolean verifica = 0 > 1;
        System.out.println("0 é maior que 1? " + verifica);
        boolean verific = 0 == 1;
        System.out.println("0 é igual que 1? " + verific);
        boolean verifi = 0 != 1;
        System.out.println("0 é diferente que 1? " + verifi);
        boolean verif = 0 <= 1;
        System.out.println("0 é menor ou igual que 1? " + verif);
        boolean veri = 0 >= 1;
        System.out.println("0 é maior ou igual que 1? " + veri);
    }
}
