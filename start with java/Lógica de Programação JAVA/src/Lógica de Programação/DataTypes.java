public class DataTypes {
    public static void main(String[] args) {
        String nome = "Vinícius";
        String sobrenome = "Chaves";
        int idade = 18;
        Double altura = 1.80;
        char sexo = 'M';
        System.out.println("Nome: " + nome);
        System.out.println("Sobrenome: " + sobrenome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);
    }
}

//String = texto, identificando com aspas duplas
//int = números inteiros
//double = números quebrados como decimais.
//float = mesma coisa do double, somente tendo que identificar com F ou f depois do número.
//char = somente aceita uma letra, sendo identificado com aspas simples